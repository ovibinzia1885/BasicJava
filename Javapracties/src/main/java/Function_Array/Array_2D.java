package Function_Array;

public class Array_2D {
    public static void main(String[] args) {
        int[][] arr={{2,3},{4,5}};
        for(int i=0;i<2;i++){
            for(int j=0;j<2;j++){
                System.out.println(("arr[" + i + "][" + j + "] = " + arr[i][j]));;
            }
        }
    }
}
